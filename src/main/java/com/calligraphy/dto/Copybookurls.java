package com.calligraphy.dto;

public class Copybookurls {

    private Integer id;

    private Integer copybookId;

    private Integer sort;

    private String url;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getCopybookId() {
        return copybookId;
    }

    public void setCopybookId(Integer copybookId) {
        this.copybookId = copybookId;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
